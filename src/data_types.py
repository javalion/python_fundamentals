
# string defined with double-quotes
first_name = "Roger"
print("String:", first_name)

# simple integer type
age = 30
print("Integer:", age)

# simple float type
price = 19.99
print("Price:", price)

# boolean type
is_happy = True
print("Boolean:", is_happy)

# none type
empty = None
print("None:", empty)

# dynamic list
dyn_list = [1, 2, 3]
print("List:", dyn_list)

# tuple (immutable list)
my_tuple = (1, 2, 3)
print("Tuple:", my_tuple)

# set
my_set = {'A', 'B', 'C'}
print("Set:", my_set)

# dictionary
my_dict = {1: 'Bear', 2: 'Cat', 3: 'Dog'}
print("Dictionary:", my_dict)

